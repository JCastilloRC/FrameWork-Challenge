package classes;

public class User{
    private int id;
    private String username;
    private String password;
    private String api_key;

    private User() {
    }
    public int getId() {
        return id;
    }
    public String getUsername() {

        return username;
    }
    public String getPassword() {

        return password;
    }
    public String getApi_key() {

        return api_key;
    }
}
